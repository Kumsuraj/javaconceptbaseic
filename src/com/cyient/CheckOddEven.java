package com.cyient;



public class CheckOddEven {

	public static void main(String[] args) {
		//using for loop.......
		for(int i=0; i<=30; i++)
       if(i%2==0)
       {
    	   System.out.println("This is even number="+i);
       }
       else {
    	   System.out.println("This not odd number="+i);
       }
	}

}
